# Bitwise and - If both values are 1 then the output will be 1 otherwise 0. But first they will convert into binary and then the operation will be performed.
print(2&3)

# Bitwise or - If both values are 0 then the output will be 0 otherwise 1. But first they will convert into binary and then the operation will be performed.
print(2|3)

# Bitwise xor - If both values are same then the output will be 0 otherwise 1. But first they will convert into binary and then the operation will be performed.
print(2^3)

# Bitwise not - It will convert 1 to 0 and 0 to 1. But first they will convert into binary and then the operation will be performed.
print(~2)